<h3 style="color:green;"><center>Read and review the job description</center></h3><br>
You’ve received a call for that dream job, so how do you prepare for the interview? The first step in the preparation process should be to go back and review the job description. Most job descriptions follow a similar pattern and are usually categorised by the following points:<br>
*Job title/Department<br>
*Duties and tasks<br>
*Skills required<br>
The job title and department will give you an understanding of the major purpose of the position and where the role fits into the organisation, allowing you to discover who your potential line manager could be.<br>
Read and review the job description very thoroughly and be sure to align your competencies with the skills required for the job. You will consequently ready yourself for questions around your previous experiences, performing similar duties in other organization.<br>
<br>
<h3 style="color:green;"><center>Plan your journey to the job interview</center></h3><br>
<p>When preparing for a job interview one of the most important things to consider is how you are going to get there. A failure to plan is a plan to fail. If you are planning on driving to the interview, make sure you fill your car with fuel the night before. You don’t want to be filling up on the way dressed in your suit.</p>
<p>Make sure you arrive on time, or better yet, at least 15 minutes early. Ensure this by knowing the address and if you can, have a trial run a couple of days before. The morning of the interview, check the traffic reports and have a backup route planned just in case. If you are travelling by train or bus, make sure you check the weather report the night before and keep an eye on the public transport websites for any delays. Look out for track works or traffic conditions that can potentially delay your train or bus trip.</p><br>







