<h3 style="color:green;"><center>Common interview questions</center></h3><br>
<div style="color: green;">1. Tell me about yourself.</div>
<div style="color:brown;"><p>This is often the first of many interview questions, designed to ‘warm up’ the candidate. Many candidates choose to respond with an overview of their work and employment history. Whilst this is helpful – especially if a manager hasn’t read your CV in detail, it’s important you offer new information, such as what are your hobbies outside of work. It’s also easy to fall into the trap of waffling. Other candidates prefer to focus on a key aspect of their career, building a story around it with performance highlights. It’s important to ensure your answer is aligned with the job description and advertisement, to demonstrate how you can add value to the company and role at hand.</p></div><br>

<div style="color: green;">2. What attracted you to our company?</div>
<div style="color:brown;"><p>This is your opportunity to demonstrate that you’ve done your research. Prior to the interview, read up on everything you can about the company from their own website, social media channels and other news articles and forums. Identify what stands out about the company’s mission and values, and how that resonates with your own desired career path and personal values.</p></div><br>

<div style="color: green;">3. Tell me about your strengths.</div>
<div style="color:brown;"><p>This should be the easiest question to prepare for. Identify two or three of your best attributes and give concrete examples of those strengths, articulating how they led to the professional success you achieved. Be sure to close the loop and articulate how they are relevant to the job you’re interviewing for.</p></div><br>

<div style="color: green;">4. What are your weaknesses?</div>
<div style="color:brown;"><p>Take the time to craft an answer that isn’t a cliché. Nothing makes a hiring manager cringe more than the answer: “I’m a perfectionist.” This is your opportunity to demonstrate your own self-awareness and desire for personal development. Try to identify something that isn’t critical to the role, and frame your answer in the positive. Want more advice? Read our article 'How to answer the interview question: What are your weaknesses?'.</p></div><br>

<div style="color: green;">5. Where do you see yourself in five years?</div>
<div style="color:brown;"><p>Your employer wants to see how driven and goal-oriented you are. They also want to check that your expectations are realistic. In your answer, demonstrate your awareness of industry trends and ability to be flexible.</p></div><br>

<div style="color: green;">6. Can you tell me about a time where you encountered a business challenge? How did you overcome it?</div>
<div style="color:brown;"><p>Behavioural question alert! This is your opportunity to use the CAR method: Context, Action, and Result. Choose an example that demonstrates where you have solved a business challenge using a skill that the employer is looking for. Provide the background, describe what action you took and the professional result you achieved.</p></div><br>

<div style="color: green;">7. What are the most important things you are looking for in your next role?</div>
<div style="color:brown;"><p>Start with your skills. Identify a skill that you exceed in and talk about how you are looking for a role that will utilise and further develop that skill. You should also explain your motivations and how this role can help you achieve your long-term goals. Always ensure that your answer is relevant to the company and the role in question.</p></div><br>

<div style="color: green;">8. Why are you leaving your current job?</div>
<div style="color:brown;"><p>It’s critical to frame your answer in the positive. Never say anything negative about your current employer, no matter how strongly your feelings for leaving are. Instead, focus on the specific, positive things that a career change to the new role will bring, for example, professional development opportunities or the excitement of a new challenge.</p></div><br>

<div style="color: green;">9. What are your salary expectations?</div>
<div style="color:brown;"><p>Do your research beforehand. Our Salary Guide can give you the latest salary trends and benchmarks. If you come prepared with reasonable salary expectations, you and your employer will know straight away if you are going to feel sufficiently compensated in the role. For more information, read our article 'When should you start discussing salary in an interview?'.</p></div><br>

<div style="color: green;">10. Do you have any questions for me?</div>
<div style="color:brown;">It’s important to come with a list of pre-prepared interview questions . Some of these might already be answered during the course of the interview, so check these off as you go, to avoid asking the same question twice.</p>
<p>If there are two things worth spending extra time on in advance of the interview, it’s behavioural interview questions and interview questions for the interviewer.</p>
<p>Behavioural interview questions are commonly used by managers to understand a candidate’s previous behaviour, as an indication of what their future behaviour might be like. While these questions can be tricky to answer, they also offer a great opportunity to demonstrate your skills. Check out the definitive guide to answer behavioural interview questions, which includes some clever acronyms to help you prepare your answers.</p>
 <p>It’s also worth spending time devising a list of interview questions to ask during the interview. It’s important that you use the interview as an opportunity to find out if the role is right for you (as well as the other way around). Asking questions during the interview also demonstrates your enthusiasm and preparedness for the role.</p>
 <p>When it comes to an interview, you can never be too prepared. Spending time carefully considering your responses in advance will bring you several steps closer to securing that role.</p></div><br>


